package dao;

public class Cliente {
    
    private int id_clte;
    private String nombre_clte;
    private String apellido1;
    private String apellido2;
    private String correo_clte;
    private String pass_clte;

    public int getId_clte() {
        return id_clte;
    }

    public void setId_clte(int id_clte) {
        this.id_clte = id_clte;
    }

    public String getNombre_clte() {
        return nombre_clte;
    }

    public void setNombre_clte(String nombre_clte) {
        this.nombre_clte = nombre_clte;
    }

    public String getApellido1() {
        return apellido1;
    }

    public void setApellido1(String apellido1) {
        this.apellido1 = apellido1;
    }

    public String getApellido2() {
        return apellido2;
    }

    public void setApellido2(String apellido2) {
        this.apellido2 = apellido2;
    }

    public String getCorreo_clte() {
        return correo_clte;
    }

    public void setCorreo_clte(String correo_clte) {
        this.correo_clte = correo_clte;
    }

    public String getPass_clte() {
        return pass_clte;
    }

    public void setPass_clte(String pass_clte) {
        this.pass_clte = pass_clte;
    }
    
    
    
    
}//fin de la clase cliente
